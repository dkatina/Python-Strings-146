
#String Concatenation : adding two or more strings together using +

word_1 = 'Big'
word_2 = 'Apple'

#Don't forget your spaces
new_york = word_1 + ' ' + word_2
print(new_york)

#repeat strings by multiplying with *

chant = 'LETS GO TEAM! '
full_chant = chant * 3
print(full_chant)


#Addition assign
#adds new value to current value and reassigns variable to combination
word = 'Hello'
word += ' there'
print(word)


#Multiply Assign
#Allows you to repeat a string x amount of times

repeat_i = 'I'
repeat_i *= 5
print(repeat_i)